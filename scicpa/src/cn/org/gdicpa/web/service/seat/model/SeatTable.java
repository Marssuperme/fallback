package cn.org.gdicpa.web.service.seat.model;

public class SeatTable {
	private String id;
	private String cpano;
	private String cpaName;
	private String trainingClass;
	private String trainingStartDate;
	private String trainingEndDate;
	private String meetingRoom;
	private String seatno;
	private String orderno;
	private String rows;
	private String columns;
	private String propertys;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getCpano() {
		return cpano;
	}
	public void setCpano(String cpano) {
		this.cpano = cpano;
	}
	public String getCpaName() {
		return cpaName;
	}
	public void setCpaName(String cpaName) {
		this.cpaName = cpaName;
	}
	public String getTrainingClass() {
		return trainingClass;
	}
	public void setTrainingClass(String trainingClass) {
		this.trainingClass = trainingClass;
	}
	public String getTrainingStartDate() {
		return trainingStartDate;
	}
	public void setTrainingStartDate(String trainingStartDate) {
		this.trainingStartDate = trainingStartDate;
	}
	public String getTrainingEndDate() {
		return trainingEndDate;
	}
	public void setTrainingEndDate(String trainingEndDate) {
		this.trainingEndDate = trainingEndDate;
	}
	public String getMeetingRoom() {
		return meetingRoom;
	}
	public void setMeetingRoom(String meetingRoom) {
		this.meetingRoom = meetingRoom;
	}
	public String getSeatno() {
		return seatno;
	}
	public void setSeatno(String seatno) {
		this.seatno = seatno;
	}
	public String getOrderno() {
		return orderno;
	}
	public void setOrderno(String orderno) {
		this.orderno = orderno;
	}
	public String getRows() {
		return rows;
	}
	public void setRows(String rows) {
		this.rows = rows;
	}
	public String getColumns() {
		return columns;
	}
	public void setColumns(String columns) {
		this.columns = columns;
	}
	public String getPropertys() {
		return propertys;
	}
	public void setPropertys(String propertys) {
		this.propertys = propertys;
	}
	
}
