package cn.org.gdicpa.web.service.contentExemptionNot.model;
/**
 *contentExemptionNot  非职业豁免学时申报 实体类
 * @author YDZ
 */
public class ContentExemptionNot {
	   private String id;          // id 
	   private String LoginId;    // 登录id
	   private String LoginName;   //登录名
	   private String sex;         // 性别
	   private String sDate;      //申报日期
	   private String sYear;      // 申报年份
	   private String EContent;     // 豁免学时内容
	   private String EHelp;       // 豁免学时说明
	   private String Status;      //省注协审核
	   private String sType;       // 区分执业和非执业会员
	   private String InitFlag;  
	   private String imeFlag;  
	   
	   public String getId() {
			return id;
	    }
		public void setId(String id) {
			this.id = id;
		}
		public String getLoginId() {
			return LoginId;
		}
		public void setLoginId(String loginId) {
			LoginId = loginId;
		}
		public String getLoginName() {
			return LoginName;
		}
		public void setLoginName(String loginName) {
			LoginName = loginName;
		}
		public String getSex() {
			return sex;
		}
		public void setSex(String sex) {
			this.sex = sex;
		}
		public String getsDate() {
			return sDate;
		}
		public void setsDate(String sDate) {
			this.sDate = sDate;
		}
		public String getsYear() {
			return sYear;
		}
		public void setsYear(String sYear) {
			this.sYear = sYear;
		}
		public String getEContent() {
			return EContent;
		}
		public void setEContent(String eContent) {
			EContent = eContent;
		}
		public String getEHelp() {
			return EHelp;
		}
		public void setEHelp(String eHelp) {
			EHelp = eHelp;
		}
		public String getStatus() {
			return Status;
		}
		public void setStatus(String status) {
			Status = status;
		}
		public String getsType() {
			return sType;
		}
		public void setsType(String sType) {
			this.sType = sType;
		}
		public String getInitFlag() {
			return InitFlag;
		}
		public void setInitFlag(String initFlag) {
			InitFlag = initFlag;
		}
		public String getImeFlag() {
			return imeFlag;
		}
		public void setImeFlag(String imeFlag) {
			this.imeFlag = imeFlag;
		}
}
