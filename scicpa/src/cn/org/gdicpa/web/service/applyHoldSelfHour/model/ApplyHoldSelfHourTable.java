package cn.org.gdicpa.web.service.applyHoldSelfHour.model;

public class ApplyHoldSelfHourTable {
	private String id;
	private String classID;
	private String hourAttachmentid;
	private String attachmentid;
	private String provinceChecked;
	private String companyId;
	private String trainingTime;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getClassID() {
		return classID;
	}
	public void setClassID(String classID) {
		this.classID = classID;
	}
	public String getHourAttachmentid() {
		return hourAttachmentid;
	}
	public void setHourAttachmentid(String hourAttachmentid) {
		this.hourAttachmentid = hourAttachmentid;
	}
	public String getAttachmentid() {
		return attachmentid;
	}
	public void setAttachmentid(String attachmentid) {
		this.attachmentid = attachmentid;
	}
	public String getProvinceChecked() {
		return provinceChecked;
	}
	public void setProvinceChecked(String provinceChecked) {
		this.provinceChecked = provinceChecked;
	}
	public String getCompanyId() {
		return companyId;
	}
	public void setCompanyId(String companyId) {
		this.companyId = companyId;
	}
	public String getTrainingTime() {
		return trainingTime;
	}
	public void setTrainingTime(String trainingTime) {
		this.trainingTime = trainingTime;
	}
	
}
