package cn.org.gdicpa.web.service.projectFill.model;

public class ProjectFill {
	private String id;
	private String companyId;
	private String companyName;
	private String projectName;
	private String fillUserId;
	private String fillUserName;
	private String fillTime;
	private String propertys;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getCompanyId() {
		return companyId;
	}
	public void setCompanyId(String companyId) {
		this.companyId = companyId;
	}
	public String getCompanyName() {
		return companyName;
	}
	public void setCompanyName(String companyName) {
		this.companyName = companyName;
	}
	public String getProjectName() {
		return projectName;
	}
	public void setProjectName(String projectName) {
		this.projectName = projectName;
	}
	public String getFillUserId() {
		return fillUserId;
	}
	public void setFillUserId(String fillUserId) {
		this.fillUserId = fillUserId;
	}
	public String getFillUserName() {
		return fillUserName;
	}
	public void setFillUserName(String fillUserName) {
		this.fillUserName = fillUserName;
	}
	public String getFillTime() {
		return fillTime;
	}
	public void setFillTime(String fillTime) {
		this.fillTime = fillTime;
	}
	public String getPropertys() {
		return propertys;
	}
	public void setPropertys(String propertys) {
		this.propertys = propertys;
	}
	
}
