package cn.org.gdicpa.web.service.checkGroupResult.model;

public class CheckGroupResultTable {
	private String checkid;
	private String checkofficeid;
	private String checkoffice;
	private String years;
	private String evaluation;
	private String prize;
	private String punish;
	private String result;

	public String getCheckid() {
		return checkid;
	}

	public void setCheckid(String checkid) {
		this.checkid = checkid;
	}
	
	public String getCheckofficeid() {
		return checkofficeid;
	}

	public void setCheckofficeid(String checkofficeid) {
		this.checkofficeid = checkofficeid;
	}

	public String getCheckoffice() {
		return checkoffice;
	}

	public void setCheckoffice(String checkoffice) {
		this.checkoffice = checkoffice;
	}

	public String getYears() {
		return years;
	}

	public void setYears(String years) {
		this.years = years;
	}

	public String getEvaluation() {
		return evaluation;
	}

	public void setEvaluation(String evaluation) {
		this.evaluation = evaluation;
	}

	public String getPrize() {
		return prize;
	}

	public void setPrize(String prize) {
		this.prize = prize;
	}

	public String getPunish() {
		return punish;
	}

	public void setPunish(String punish) {
		this.punish = punish;
	}

	public String getResult() {
		return result;
	}

	public void setResult(String result) {
		this.result = result;
	}
}
